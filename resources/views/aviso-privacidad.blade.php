    <p>Para una compañía innovadora y de carácter nacional como Industrias Salcom, S.A. de C.V. (en adelante "Salcom") el tratamiento legítimo, controlado e informado de sus datos personales es de vital importancia para alcanzar los objetivos corporativos a través de todas las áreas del negocio y reiterar nuestro compromiso con la privacidad y su derecho a la autodeterminación informativa.</p>

    <p>El presente Aviso de Privacidad tiene como objeto informarle sobre el tratamiento que se le dará a sus datos personales cuando los mismos son recabados, utilizados, almacenados y/o transferidos por Salcom, lo cual se lleva a cabo de conformidad con las disposiciones de la Ley Federal de Protección de Datos Personales en Posesión de los Particulares (en adelante la "Ley").</p>

    <h2>1. Datos Personales o información personal</h2>
    <p>Algunos de los datos personales y/o datos personales sensibles que nos proporcione pueden ser:</p>
    <ul>
        <li><strong>Datos de identificación:</strong> nombre, domicilio, teléfono, correo electrónico, CURP, fecha de nacimiento, edad, nacionalidad, imagen, fotografía, país de residencia, número de seguridad social.</li>
        <li><strong>Datos laborales:</strong> puesto, domicilio laboral, correo electrónico, teléfono y fax del trabajo, contrato corporativo, nombre del empleador.</li>
        <li><strong>Datos de educación:</strong> trayectoria educativa, título, número de cédula profesional, especialidad, certificados de estudios.</li>
        <li><strong>Datos de salud:</strong> estado de salud, historial clínico, enfermedades o padecimientos.</li>
        <li><strong>Datos bancarios y de facturación:</strong> número de cuenta, CLABE, RFC, datos de facturación.</li>
    </ul>

    <h2>2. Datos personales de menores de edad</h2>
    <p>Salcom no obtiene, usa, divulga o almacena información relacionada con menores de edad, sin el consentimiento previo de sus padres o tutores.</p>

    <h2>3. Finalidades y/o Usos de los datos personales</h2>
    <p>Si usted es un <strong>proveedor</strong>, sus datos se utilizarán para: registrarlo como proveedor, redactar los instrumentos legales necesarios, realizar pagos y llevar a cabo la facturación.</p>
    <p>Si usted es un <strong>consumidor o posible consumidor</strong>, sus datos se utilizarán para: cumplir con obligaciones derivadas de una relación jurídica, hacer cumplir contratos, cumplir con obligaciones legales, fines estadísticos y científicos.</p>

    <h2>4. Otra información no personal</h2>
    <p>Recibimos y almacenamos cierta información cuando usted interactúa en nuestros sitios web, como su dirección IP, sistema operativo y tipo de navegador. Se pueden utilizar "cookies" para brindarle una mejor experiencia de navegación.</p>

    <h2>5. Enlaces desde nuestros sitios web</h2>
    <p>Nuestros sitios web pueden contener enlaces a otros sitios que no pertenecen a Salcom. No garantizamos ni nos hacemos responsables por el contenido en dichos enlaces.</p>

    <h2>6. Medidas de Seguridad</h2>
    <p>Salcom ha implementado y mantiene las medidas de seguridad, técnicas administrativas y físicas, necesarias para proteger sus datos personales.</p>

    <h2>7. Transferencias de datos personales</h2>
    <p>Salcom únicamente transferirá sus datos personales a terceros relacionados jurídica o comercialmente con Salcom para cumplir con las finalidades descritas.</p>

    <h2>8. Derechos del titular (ARCO)</h2>
    <p>Usted puede ejercitar los derechos de Acceso, Rectificación, Cancelación y Oposición respecto a sus datos personales.</p>
    <ul>
        <li><strong>Acceso:</strong> que se le informe cuáles de sus datos están en nuestras bases de datos.</li>
        <li><strong>Rectificación:</strong> que se corrijan o actualicen sus datos.</li>
        <li><strong>Cancelación:</strong> que sus datos sean dados de baja.</li>
        <li><strong>Oposición:</strong> oponerse al tratamiento de sus datos.</li>
    </ul>

    <h2>9. Modificaciones al Aviso de Privacidad</h2>
    <p>Salcom se reserva el derecho de modificar el presente Aviso de Privacidad. Los avisos actualizados se publicarán en el sitio web.</p>

